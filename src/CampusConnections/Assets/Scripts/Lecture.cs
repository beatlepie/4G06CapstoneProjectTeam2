public class Lecture
{
    public string code;
    public string name;
    public string instructor;
    public string time;
    public string location;

    public Lecture(string code, string name, string instructor, string time, string location)
    {
        this.code = code;
        this.name = name;
        this.instructor = instructor;
        this.time = time;
        this.location = location;
    }
}
